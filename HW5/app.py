from fastapi import FastAPI, HTTPException, Request
from pydantic import BaseModel
from fastapi.responses import HTMLResponse
from fastapi.templating import Jinja2Templates

app = FastAPI()
templates = Jinja2Templates(directory="templates")


class User(BaseModel):
    id: int
    name: str
    email: str
    password: str


users = []
for i in range(10):
    new_user = User(id=i, name=f'User {i + 1}', email=f'user{i + 1}@mail.ru', password=f'user{i + 1}')
    users.append(new_user)


@app.get("/", response_class=HTMLResponse)
async def hello(request: Request):
    return templates.TemplateResponse("hello.html", {"request": request, 'title': '–ì–ª–∞–≤–Ω–∞—è'})


@app.post("/users/")
async def add_user(user: User):
    users.append(user)
    return user


@app.put("/users/{user_id}")
async def update_user(user_id: int, user: User):
    for u in users:
        if u.id == user_id:
            u = user
    return user


@app.delete("//users/{user_id}")
async def delete_user(user_id: int):
    if user_id <= len(users):
        return {"User delete": users.pop(user_id)}
    return HTTPException(status_code=404, detail='User not found')


@app.get("/users/", response_class=HTMLResponse)
async def read_users(request: Request):
    return templates.TemplateResponse("users.html", {"request": request, 'users': users, 'title': '–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–∏'})